
public class BasicMath {
    public static void main(String args[]){
        int a = 1 + 2;
        int b = a * 2;
        int c = b/4;
        int d = c-b;
        int e = -d;
        System.out.println("a = "+ a + " b = " + b + " c = "+ c + " d = " + " e = " + e);
        double da = 1 + 2;
        double db = da * 2;
        double dc = db/4;
        double dd = dc - db;
        double de = -dd;
        System.out.println("da = "+ da + " db = " + db + " dc = "+ dc + " dd = " + " de = " + de);

    }
}
